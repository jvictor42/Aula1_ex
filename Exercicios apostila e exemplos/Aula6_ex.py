#Usando Turtle
import turtle
jn=turtle.screen() #definindo uma variavel da função Screen. Vai abrir uma tartaruga em uma tela branca. Ela tem "metodos" (andar, voltar, direita)
joana=turtle.Turtle() # damos o nome a nossa tartaruga (que na vdd é uma seta)
joana.forward(50) #move a tartaruga pra frente 50
joana.left(90) #move gira 90 graus
joana.forward(50) #frente de novo
#------
jn.bgcolor("lightgreen") #esse metodo muda a cor da janela ( a string é um argumento)
jn.title("Oláaaaa") #muda o nome da janela

teca=turtle.Turtle() #criamos uma nova tartaruga chamada Teca ( uma nova "instancia"/ objeto do tipo Turtle)
